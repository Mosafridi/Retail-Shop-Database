Create database retailShop;

CREATE TABLE StoreBranch (
    storeNumber INT NOT NULL AUTO_INCREMENT,
    city VARCHAR(20),
    state VARCHAR(20),
    country VARCHAR(20),
    addressLine1 VARCHAR(30),
    addressLine2 VARCHAR(30),
    postalCode VARCHAR(20),
    PRIMARY KEY (storeNumber)
);

CREATE TABLE Staff (
    staffId INT NOT NULL AUTO_INCREMENT,
    fName VARCHAR(20),
    lName VARCHAR(20),
    email VARCHAR(40),
    phoneNumber VARCHAR(20),
    position VARCHAR(30),
    hireDate DATE,
    storeNumber INT,
    PRIMARY KEY (staffId),
    FOREIGN KEY (storeNumber) REFERENCES StoreBranch(storeNumber)
);

CREATE TABLE Supplier (
    supplierId INT NOT NULL AUTO_INCREMENT,
    supplierName VARCHAR(40),
    contactName VARCHAR(30),
    phoneNumber VARCHAR(20),
    email VARCHAR(30),
    address VARCHAR(40),
    PRIMARY KEY (supplierId)
);

CREATE TABLE Product (
    productCode VARCHAR(50) NOT NULL,
    productName VARCHAR(20),
    productDescription VARCHAR(30),
    quantityInStock INT,
    retailPrice DECIMAL(10, 2),
    productionCost DECIMAL(10, 2),
    supplierId INT,
    PRIMARY KEY (productCode),
    FOREIGN KEY (supplierId) REFERENCES Supplier(supplierId)
);

CREATE TABLE Customer (
    customerId INT NOT NULL AUTO_INCREMENT,
    fName VARCHAR(20),
    lName VARCHAR(20),
    email VARCHAR(40),
    phoneNumber VARCHAR(20),
    PRIMARY KEY (customerId)
);

CREATE TABLE Training (
    trainingId INT NOT NULL AUTO_INCREMENT,
    staffId INT,
    courseName VARCHAR(50),
    trainingDate DATE,
    completionStatus VARCHAR(20),
    validUntil DATE,
    PRIMARY KEY (trainingId),
    FOREIGN KEY (staffId) REFERENCES Staff(staffId)
);

CREATE TABLE Inventory (
    inventoryId INT NOT NULL AUTO_INCREMENT,
    productCode VARCHAR(50),
    receivedDate DATE,
    quantityReceived INT,
    currentStock INT,
    PRIMARY KEY (inventoryId),
    FOREIGN KEY (productCode) REFERENCES Product(productCode)
);

CREATE TABLE Orders (
    orderId INT NOT NULL AUTO_INCREMENT,
    supplierId INT,
    orderDate DATE,
    totalCost DECIMAL(10, 2),
    deliveryDate DATE,
    PRIMARY KEY (orderId),
    FOREIGN KEY (supplierId) REFERENCES Supplier(supplierId)
);

CREATE TABLE Sales (
    saleId INT NOT NULL AUTO_INCREMENT,
    productCode VARCHAR(50),
    customerId INT,
    saleDate DATE,
    quantity INT,
    totalPrice DECIMAL(10, 2),
    staffId INT,
    PRIMARY KEY (saleId),
    FOREIGN KEY (productCode) REFERENCES Product(productCode),
    FOREIGN KEY (customerId) REFERENCES Customer(customerId),
    FOREIGN KEY (staffId) REFERENCES Staff(staffId)
);

    
